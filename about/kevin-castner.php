  <h1>Kevin Castner &ndash; Executive Director</h1>
  <p class="bodyCopy">Kevin Castner is the Executive Director and an educational planning strategist with Cambridge Strategic Services. Kevin offers expertise in strategic planning and educational programming to accomplish the strategic intent of each school system’s mission and vision.</p>
  <p class="bodyCopy">In related efforts, Kevin worked closely with the Texas Association of School Administrators as part of the Visioning Institute, a mission driven forum for 35 of Texas’ leading superintendents to develop a common vision and plan of action for the future of Texas Public Schools. In 2010, he served as a Race to the Top Peer Review Panelist for the U.S. Department of Education. During the process he reviewed 8 states including several finalists. In addition to serving as an adjunct instructor for the University of Virginia, Kevin has worked with several superintendents as part of the Virginia Association of School Superintendents’ Executive Coaching Service and served several times as a superintendent search consultant for BWP and Associates.</p>
  <p class="bodyCopy">Kevin’s 40 plus years in public education includes 11 years as superintendent in Albemarle County, Virginia and 8 years as associate/deputy superintendent in Frederick County, Maryland. While in Albemarle County, Kevin led the development of a framework for quality learning that was based on Understanding by Design, the work of McTighe and Wiggens, and promoted lifelong learning skills. During Kevin’s 25 years in Maryland, he initiated the Maryland Assessment Consortium which involved all Maryland counties, and resulted in performance assessment test banks that supported the Maryland School Performance curriculum frameworks.</p>
  <p class="bodyCopy">Kevin’s experience in working at all levels of public school systems provides him with a deep understanding of the vertical and horizontal challenges and opportunities PK – 16 learning communities face today. He has a passion to help those communities face their challenges and realize their vision, mission and goals.</p>
  <p class="bodyCopy">Kevin holds a Bachelor of Science degree from Marquette University, a Master’s degree from the University of Maryland, and a doctorate in Educational Leadership for George Washington University.</p>
