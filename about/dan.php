		<h1>David Baldner, M.S. &ndash; Vice President</h1>
		<p>David has served the education profession since 1982 beginning his career as a middle school social studies teacher in Galena Park ISD, the district he attended as a student.  After two years, he was invited to teach in the district’s gifted and talented program where he began to learn a tremendous amount about facilitation with classroom activities.  After three-and-a-half years in the G/T program, David became an assistant principal and then became a principal, where he served from 1990 - 1995.  In 1996, he moved to central office as the Director of Elementary Schools where he was given the responsibility for strategic planning for the school district as a whole. During that time, David was trained in facilitation techniques for strategic planning, and he was also asked to participate in the district’s initial investigation in the quality movement.</p>
		<br />
		<p>In 1998 David moved to the regional service center in the Houston area as the Director of Campus and District Leadership where he focused on training and consulting assistance in areas such as quality, leadership, professional assessment, and coaching.</p>
		<br />
		<p>David moved to Lubbock in 2000 where he served at the regional service center, expanding his experiences in strategic planning by working closely with Cambridge and continuing activity in the areas of organizational quality, process management through ISO 2001, leadership and team-building trainings, and a host of other areas of responsibility.  He firmly believes the Cambridge process is integral in its application to each of these areas, and he sees a direct link to organizational performance based on the foundation set by a sound strategic plan and planning process.</p>
		<br />
		<p>David received his BA in History/Political Science from Texas Lutheran University, his MS in Educational Leadership from the University of Houston-Clear Lake, and his superintendent’s certificate from Texas Tech University.</p>
