    <h1>BISD strategic planning kicks off</h1>

    <p><a href="http://bastropadvertiser.com/2010/09/12/bisd-strategic-planning-kicks-off" target="_blank">By The Bastrop Advertiser</a></p>

    <p>After a summary of what a strategic educational plan for the Bastrop Independent School District is meant to accomplish, a senior strategist with Cambridge Strategic Services, asked the audience gathered at Cedar Creek High School Thursday night what they viewed as important to student's education.<br/>
        <br />
      "What are your aspirations for students in BISD?" the senior strategist asked. Her firm has been hired by BISD to help implement a plan.<br/>
      <br />
      Among the answers from an assembly of parents and educators were teaching students foreign languages at a young age, having students college-ready if they choose that route, learning business ethics, being good communicators and having more outdoor/nature opportunities.<br/>
      <br />
      "You have very high aspirations," the senior strategist complimented the audience of about 40 people. <br />
      <br />
      It was one of the first major steps as the Bastrop Independent School District shapes a strategic education plan to ensure the district keeps pace with the 21st century.<br/>
      <br />
      With the district already having formed a 30-member strategic planning committee composed of community and BISD staff, Thursday night's meeting was the kickoff to the formation of "action planning teams." <br />
      <br />
      These teams are expected to form approximately five to seven educational strategies that the strategic planning committee will work on implementing. <br />
      <br />
      The BISD school board will have final approval on implementing the recommended strategic plan.<br />
      <br />
      "We're looking to get about 25 members per action team, so that's about 125 to 175 people, depending on whether there's five to seven recommended strategies," said Donald Williams, BISD communications director. He added that community enthusiasm for participating in the project is already evident – from the 40 people who showed up Thursday to the approximate 75 additional people who have already contacted the district, saying they would like to participate. <br />
      <br />
      In her introductory remarks the senior strategist told the audience that educators and their supporting communities needed to keep pace with rapidly-advancing technology. <br />
      <br />
    <blockquote>"Students in school today are in competition with students who graduate from all over the world," she said. The senior strategist said traditional teaching methods needed revamping.</blockquote>
    <br />
    <p>According to the senior strategist, location learning, where students learned at one time, in one place and in the same way, based on age, was how education operated in the 20th century – a strategy that must change. <br />
        <br />
      She cited a line from a book titled, "How to Bring Our Schools Out of the 20th Century," which states that "If Rip Van Winkle woke up today, the only place he would recognize is school." <br />
      <br />
      But the senior strategist also emphasized that the action planning teams and the strategic planning committee will likely find some current educational methods that they will want to keep. <br />
      <br />
    <blockquote>"Strategic planning is not about eliminating everything you're doing," the senior strategist said. "The harder part of strategic planning is identifying what is taking up resources but not helping the district reach its highest aspirations."</blockquote>
    <br />
    <p>Also giving input during the session was a panel of six BISD students: Shagufta Khan, Erin Rainosek, Hillari Raemsch, Jordan Visage, Joelle Foster and Elizabeth Duggan. <br />
        <br />
      The students provided input on what they like about current education and what improvements they would like to see. <br />
      <br />
      "At Bastrop schools, there's a lot of teamwork, which makes communication between people better," Raemsch told the audience. <br />
      <br />
      Visage praised teachers.

      "There's a lot of teacher involvement with students, like how they've helped me with college applications," she said. <br />
    </p>
