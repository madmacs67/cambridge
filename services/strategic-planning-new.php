    <h1>Strategic Planning</h1>
    <p>The Strategic Planning Team consists of 25 to 30 people who collectively reflect all the values and perspectives of the district. The team is made up of board members, administrators, teachers, other staff, community members, parents, and students. After collecting data regarding the organization’s distinctive characteristics, the Planning Team is sequestered for three days to begin the actual planning process. During this meeting, all the following components of the plan are developed in draft form:<br />
</p>
<br />
<p><strong>Beliefs</strong></p>
<p>A statement that is a formal expression of the organization’s (community’s) fundamental values: its ethical code, its overriding convictions, its inviolate commitments.</p>
<br />
<p><strong>Mission</strong></p>
<p>A statement that is a clear and concise expression of the district’s identity, purpose, and the means of action.</p>
<br />
<p><strong>Strategic Parameters</strong></p>
<p>Limitations the organization places upon itself. They are things the organization either will never do or will always do. The intent is concentration of effort on the mission and objectives.</p>
<br />
<p><strong>Objectives</strong></p>
<p>The planning organization’s commitment to achieve specific, measurable end results in terms of student success, achievement and/or performance.</p>
<br />
<p><strong>Strategies</strong></p>
<p>The most important part of the planning discipline. In particular, the articulation of bold initiatives through which the organization will deploy its resources toward the stated mission and objectives.</p>
<br />
<p><strong>In addition, the team conducts extensive analyses of:</strong></p>
<ul>
    <li><strong>Internal Factors:</strong> A thorough, unbiased, tripartite examination of the organization: strengths, weaknesses, and a critique of the organizational design.</li>
    <li><strong>External Factors:</strong> An examination of those forces which an organization has little or no control, such as social, political, economic, demographic, technological, or educational trends.</li>
    <li><strong>Competition:</strong> Any other organization providing the same service in the marketplace.</li>
    <li><strong>Critical Issues:</strong> Threats and opportunities redefined strategically.</li>
    </ul>

