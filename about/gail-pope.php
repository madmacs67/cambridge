		<h1>Gail Elizabeth Pope, Ed.D.</h1>
		<p class="bodyCopy">Gail Elizabeth Pope earned a Bachelor of Science degree from Radford University and Master’s and Doctorate degrees from Texas A&M University. She has taught in elementary, middle, and high school in Mississippi, Ohio, and Texas, as well as at Old Dominion University where she taught a graduate class in strategic planning. She began her career in education administration in Spring Branch ISD in Houston, Texas where she served as high school assistant principal and middle school principal. She continued in administration in Virginia Beach City Schools before becoming a director of the Governor’s Best Practice Center with the Virginia Department of Education. Pope then served as the Associate Superintendent for Curriculum and Instruction for Manassas Park City Schools. She then served for six years as superintendent of Manassas City Schools.</p>
