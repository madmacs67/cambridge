    <h1>District  to Shape Future Direction of NKC Schools </h1>

    <p><a href="http://nkcschools.blogspot.com/2011/01/district-stakeholders-to-shape-future.html" target="_blank">By news@nkcs</a></p>

    <p>North Kansas City Schools is a nationally accredited school district with many accomplishments to its credit. Students continue to make strong achievement gains, and operationally the District is leaner, more effective and more efficient than in years past.</p>
<br />
<p>Yet, as the sixth largest school district in Missouri, it also faces challenges such as declining local and state revenues, a growing student enrollment, finite classroom space, an aging digital network infrastructure and limitations on bonding capacity. Each of these needs strategic direction to ensure continued success with students.</p>
<br />
<p>With that in mind, the NKC Schools Board of Education recently endorsed a proposal to develop and implement a community-driven, results-based strategic plan. To ensure the process is fresh, objective and inclusive, the Board is partnering with Cambridge Strategic Services, a nationally recognized strategic planning firm with special expertise in public education. In fact, Cambridge Strategic Services literally wrote the book on the subject – “Strategic Planning for America’s Schools”.</p>
<br />
<p>Superintendent Todd White recommended the use of a professional, external facilitator to shepherd the process. He explained, “We need a clear and honest understanding of our community’s expectations for their schools. Cambridge Strategic Services will ensure that the critical issues are identified, objectively analyzed and converted into realistic action plans.”</p>
<br />
<p>All planning activity will be guided by what is in the best interest of students and will be done with the full involvement of the school board and the community. There will be an emphasis on measurable and demonstrable results, accountability for performance effectiveness, and continuous evaluation.</p>
<br />
<p>The process will begin its initial phase in the spring and is slated to be completed during the 2011-12 school year.</p>
