    <h1>Strategic Planning Software</h1>
    <p>Our strategic planning software will allow the district to manage their entire Strategic Plan electronically from the initial development of the plan to the monitoring of progress toward completion of the action plans and the achievement of the districts strategic objectives.</p>

      <br />
      <p>The software assists administration in ensuring the implementation of the plan through attaching all aspects of the plan to the annual work plan of those assigned responsibility for implementation.  It provides instant access to the entire strategic plan with reports on current status of each action plan along with information as to who is responsible for that portion of the plan.</p>

      <br />
      <p>Each action step of the strategic plan can be budgeted through our strategic planning software with costs projected over a five year span. It provides administration the capability of determining impact on the budget by adjusting the start date of each action plan and including assumptions for increase costs over the life of the action plan implementation.</p>

      <br />
      <p>The district can demonstrate accountability for the plan with online access to the public for any part of the strategic plan the administration chooses to make available. Access can be accommodated through “linking” with district website and has the capability of providing “real time” information to the public.</p>

      <br />
      <p>Our strategic planning software also provides a total Site Planning component that allows administration to track every site and provides a link between what is taking place district-wide and what is supporting the district plan at the site level.  It will ensure the site planning reflects what is occurring at the district level while allowing each site to emphasize it’s uniqueness.</p>

      <br />
      <p><strong>Our strategic planning software was designed for ease of use and flexibility. There are many features that make this a valuable tool for school districts.</strong><br />
	  </p>
    <ul>
      <li class="first">Web-based for easy access from anywhere that provides internet access</li>
      <li>Works well on both Mac and PC environments</li>
      <li>Districts can apply their own design to fit with other district documents and websites</li>
      <li>Sites have the ability to “personalize” the site planning feature with their own logos, mascots, etc.</li>
      <li>Information of progress easily available in either a text or graph format</li>
      <li>Variety of graphing options available to report progress on strategic objectives or progress on action plan implementation</li>
      <li>Presentation module facilitates development of presentation options to suit the audience</li>
      </ul>
