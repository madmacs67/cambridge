    <h1>Strategic plan gets new look</h1>
    <p><strong>By Carol Christian, Houston Chronicle</strong></p>
    <p>Continuing a process that began in May, the Pasadena school district has issued a first-draft update of its 1989 strategic plan.</p>
    <br />
    <p>The entire revision is expected to take about 18 months, ending in early 2011.</p>
    <br />
    <p>A 30-member committee of community members, students, teachers and administrators worked last month on the first draft, which has four sections: beliefs, mission statement, strategic objectives and strategies.</p>
    <br />
    <p>The draft, as presented Oct. 7 to the school board, is posted on the district website at <a href="http://www.pasadenaisd.org" target="_blank">www.pasadenaisd.org</a>.</p>

	<br />
    <p>Over the next four to five months, separate groups will develop action plans for each of seven proposed strategies:</p>
    <br />
    <ul>
       <li>Ensure rigorous curriculum and relevant learning experiences that promote real-life application.</li>
       <li>Provide flexible learning environments, structures and schedules that meet the needs of all 21st Century learners.</li>
       <li>Develop and promote 21st Century career and college exploration and preparation programs for all students.</li>
       <li>Recruit, develop and retain a highly qualified staff.</li>
       <li>Pursue a culturally responsive approach to meaningful parental engagement.</li>
       <li>Ensure vital and ongoing relationships with business and community partners.</li>
       <li>Install appropriate technologies throughout the district to support instructional and administrative needs.</li>
    </ul>
    <br/>
    <p>When the action plans are finished, the 30-member committee will reconvene to decide whether to accept or reject each action plan. The committee can also suggest changes in the action plans.</p>
    <br />
    <p>The school board must approve the new strategic plan before it can be implemented.</p>
    <br />
    <p>Implementation is expected to be a five-year process with annual updates, according to the district website.</p>
    <br />
    <p>The goal for the next strategic plan, said Pasadena ISD Superintendent Kirk Lewis, is to redesign and reconfirm the district's mission. </p>

